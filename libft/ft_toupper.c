#include "includes/libft.h"

int	ft_toupper(int c)
{
	unsigned int d;

	d = (unsigned int)c;
	if (d >= 97 && d <= 122)
	{
		d = d - 32;
		return ((int)d);
	}
	else
		return (c);
}
