#include "includes/libft.h"

int	ft_intlen(int n)
{
	int x;

	if (n == -2147483648)
		return (11);
	x = 0;
	if (n < 0)
	{
		n = -n;
		x++;
	}
	while (n >= 10)
	{
		x++;
		n = n / 10;
	}
	if (n < 10)
		x++;
	return (x);
}
