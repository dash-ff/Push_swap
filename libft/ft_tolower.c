#include "includes/libft.h"

int	ft_tolower(int c)
{
	unsigned int d;

	d = (unsigned int)c;
	if (d >= 65 && d <= 90)
	{
		d = d + 32;
		return ((int)d);
	}
	else
		return (c);
}
