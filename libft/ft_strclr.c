#include "includes/libft.h"

void	ft_strclr(char *s)
{
	int i;
	int k;

	i = 0;
	if (s == NULL)
		return ;
	k = ft_strlen(s);
	while (i != k)
	{
		s[i] = '\0';
		i++;
	}
}
